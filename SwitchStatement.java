public class SwitchStatement {
	public static void main(String[] args) {
    	
    	char alphabet='f';
    	
    	switch(alphabet){
    		case 'a':
    			System.out.println("A");
    			break;
    		case 'b':
    			System.out.println("B");
    			break;
    		case 'c':
    			System.out.println("C");
    			break;
    		case 'd':
    			System.out.println("D");
    			break;
    		case 'e':
    			System.out.println("E");
    			break;
    		default:
    			System.out.println("Not Found");
    			break;
    	}
	}
}